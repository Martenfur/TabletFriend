
namespace TabletFriend
{
	public enum ButtonVisibility
	{
		/// <summary>
		/// The button will always be present.
		/// </summary>
		Always = 0,

		/// <summary>
		/// The button will only be present in docked mode.
		/// </summary>
		Docked = 1,

		/// <summary>
		/// The button will only be present in undocked mode.
		/// </summary>
		Undocked = 2
	}
}
